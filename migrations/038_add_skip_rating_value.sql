-- ============================================================================
-- rating_type enum に 'skip' を追加
-- ============================================================================
-- 実行先: Supabase (PostgreSQL 15+)
-- 前提: 037 まで実行済み
--
-- 背景:
--   現状の評価タブ「知らない / スキップ」ボタンは DB に何も書かないため、
--   リロード/再フェッチで同じ曲が即座に再出現する (= 推薦体験を著しく損ねる)。
--
--   永久除外ではなく TTL 20 日で自動的に再評価チャンスを与えたいので、
--   evaluations テーブルに rating='skip' の行を入れて updated_at で TTL 判定する。
--   学習信号 (user_genre_distribution / user_artist_pref) は既に
--   rating IN ('easy','medium','practicing') で絞っているため、
--   'skip' を追加してもユーザー嗜好プロフィールには影響しない。
--
-- 注意:
--   PG ≤11 では同一トランザクション内で追加した enum 値を使用できないため、
--   この migration では値の追加のみを行い、関数側の更新は次の migration で行う。
-- ============================================================================

ALTER TYPE public.rating_type ADD VALUE IF NOT EXISTS 'skip';

NOTIFY pgrst, 'reload schema';

-- ============================================================================
-- マイグレーション完了
-- ============================================================================
